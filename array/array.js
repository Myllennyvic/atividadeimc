const array = [21, 6, 84, 24, 86, 11]
console.log(array)
//array -> métodos comuns

//inserir dados//
array.push(5) // inserir ao final do array. VOCÊ PRECISA POR UM VALOR 
console.log(array)
array.unshift(5) // inserir elementos no começo do array . VOCÊ PRECISA POR UM VALOR 
console.log(array)

//remover dados//
array.pop() // remover o elemento final do array 
console.log(array)
array.shift() // remover o elemento do começo do array 
console.log(array)
